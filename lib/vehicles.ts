// ============================================================
// UpFit Vehicle Database
// ============================================================
// Architecture: 4-layer data model
//
// Layer 1 — Vehicle identity (what car, what generation)
// Layer 2 — Factory equipment flags (what came standard)
// Layer 3 — Configurator rules (what to show/offer/hide)
// Layer 4 — Content (SEO copy, caveats, enthusiast notes)
//
// Aerpro API supplements this at runtime for:
//   - Validation of known part numbers
//   - Gap-filling for vehicles not yet in this DB
//
// Confidence levels:
//   "high"     = Aerpro part numbers confirmed, installer verified
//   "medium"   = Aerpro catalogue mapped, installer unverified
//   "low"      = Research-based, needs Aerpro confirmation
//   "api-only" = Not in our DB — fall through to Aerpro API
// ============================================================

// ── Types ────────────────────────────────────────────────────

export type Confidence = "high" | "medium" | "low" | "api-only";
export type Difficulty = "easy" | "medium" | "hard";
export type RecommendedPath = "module" | "replacement" | "adapter" | "quote";

export type VehicleGeneration = {
  // Identity
  id: string;
  slug: string;
  label: string;
  yearFrom: number;
  yearTo: number;

  // Layer 2 — Factory equipment
  factory: {
    hasScreen: boolean;
    screenDescription: string | null;    // "7\" MyLink", "8\" SYNC3" etc
    hasCarPlay: boolean;                 // wired factory CarPlay
    hasWirelessCarPlay: boolean;
    hasCamera: boolean;                  // factory reverse camera
    hasParkingSensors: boolean;
    hasNav: boolean;
    navSystemName: string | null;        // "MyLink", "SYNC3", "MZD Connect"
    hasPremiumAudio: boolean;
    premiumAudioBrand: string | null;    // "Bose", "JBL", "Rockford Fosgate"
    has360Camera: boolean;
  };

  // Layer 3 — Configurator rules
  configurator: {
    moduleAvailable: boolean;
    moduleRecommended: boolean;          // prefer module over replacement
    cameraRetentionAvailable: boolean;   // existing camera can be kept
    showCameraOption: boolean;           // false if camera auto-retained
    showSensorsOption: boolean;
    requiresQuote: boolean;
    quoteReason: string | null;
  };

  // Pricing — all inc GST, null = not available / quote only
  pricing: {
    installedBase: number | null;        // head unit + fascia + install
    installedWithCamera: number | null;
    installedWithSensorsRear: number | null;
    installedWithSensorsFrontRear: number | null;
    moduleInstalled: number | null;
    diyBase: number | null;              // parts only — null until ecommerce
    diyWithCamera: number | null;
  };

  // Aerpro data
  aerpro: {
    fasciaPartNumber: string | null;
    harnessPartNumber: string | null;
    swcInterface: string | null;
    antennaPatchLead: string | null;
    cameraKitPartNumber: string | null;
    vehicleSelectorId: string | null;
    productUrl: string | null;
    confidence: Confidence;
  };

  // Layer 4 — Content
  content: {
    installTimeMin: number;              // minutes
    installTimeMax: number;
    difficulty: Difficulty;
    recommendedPath: RecommendedPath;
    recommendedPathReason: string;
    whatIsLost: string[];
    whatIsRetained: string[];
    caveat: string | null;
    enthusiastNote: string | null;
    audioUpgradeNote: string | null;
  };
};

export type VehicleModel = {
  id: string;
  slug: string;
  name: string;
  generations: VehicleGeneration[];
};

export type VehicleBrand = {
  id: string;
  slug: string;
  name: string;
  models: VehicleModel[];
};

// ── Helper ───────────────────────────────────────────────────

/** Returns the generation matching a year string like "2015–2020" */
export function getGeneration(
  brand: VehicleBrand,
  modelSlug: string,
  generationSlug: string
): VehicleGeneration | undefined {
  const model = brand.models.find((m) => m.slug === modelSlug);
  return model?.generations.find((g) => g.slug === generationSlug);
}

export function getBrandBySlug(slug: string): VehicleBrand | undefined {
  return vehicles.find((b) => b.slug === slug);
}

export function getModelBySlug(
  brandSlug: string,
  modelSlug: string
): VehicleModel | undefined {
  return getBrandBySlug(brandSlug)?.models.find((m) => m.slug === modelSlug);
}

